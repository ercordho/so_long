#include "../../inc/so_long.h"

int	ft_isalpha(int c)
{
	if (c >= (int) 'a' && c <= (int) 'z')
		return (1);
	if (c >= (int) 'A' && c <= (int) 'Z')
		return (1);
	return (0);
}
